export function getQuestionsByTheme(subject: string, theme: string, count = 10): Question[] {
  const filtered = QUESTIONS.filter(
    (q) => q.subject === subject && q.theme === theme
  );
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function getSubjectInfo(id: string): SubjectInfo | undefined {
  return SUBJECTS.find((s) => s.id === id);
}
